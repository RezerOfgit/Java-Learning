public class BitOperator02{
	public static void main(String [] args){
		//13&7=?
		//13 的补码：0000 0000 0000 1101
		//7 的补码：0000 0000 0000 0111
		//13&7的补码：0000 0000 0000 0101
		//结果原码：0000 0000 0000 0101->5
		System.out.println(13&7);
		
		// 5|4=？
		// 5 的补码：0000 0000 0000 0101
		// 4 的补码：0000 0000 0000 0100
		// 5|4的补码：0000 0000 0000 0101
		// 结果原码：0000 0000 0000 0101->5
		System.out.println(5|4);//5

		// -3^3=？
		// 3 的补码：0000 0000 0000 0011
		// -3的原码：1000 0000 0000 0011
		// -3的反码：1111 1111 1111 1100
		// -3的补码：1111 1111 1111 1101
		// -3^3的补码：1111 1111 1111 1110
		// -3^3的反码：1111 1111 1111 1101
		// -3^3的原码：1000 0000 0000 0010->-2
		System.out.println(-3^3);//-2
	}
}