//BitOperator.java

public class BitOperator{
	public static void main(String [] args){
		// 为了验证你是否真的看懂了 P98，请尝试推导 -5 的三种码。
		// 	(提示：5 是 0000 0101，那 -5 呢？)
		// 原码：1000 0101

		// 反码：1111 1010

		// 补码：1111 1011
		
		//2&3
		//2 的原码：0000 0000 0000 0010
		//3 的原码：0000 0000 0000 0011
		//2&3的补码：0000 0000 0000 0010
		//2&3的原码：0000 0000 0000 0010->2
		System.out.println(2&3);//2

		//~-2=?
		//2 的原码：0000 0000 0000 0010
		//-2的原码：1000 0000 0000 0010
		//-2的反码：1111 1111 1111 1101
		//-2的补码：1111 1111 1111 1110
		//~-2的补码：0000 0000 0000 0001
		//~-2的原码：0000 0000 0000 0001
		System.out.println(~-2);//1

		//~2=？
		//2 的原码：0000 0000 0000 0010
		//2 的补码：0000 0000 0000 0010
		//~2的补码：1111 1111 1111 1101，负数
		//~2的反码：1111 1111 1111 1100
		//运算后的原码：1000 0000 0000 0011->-3
		System.out.println(~2);//-3

		//2|3=?
		//2 的原码：0000 0000 0000 0010
		//3 的原码：0000 0000 0000 0011
		//2|3的原码：0000 0000 0000 0011->3
		System.out.println(2|3);//3

		//2^3=?
		//2 的原码：0000 0000 0000 0010
		//3 的原码：0000 0000 0000 0011
		//2^3 的原码：0000 0000 0000 0001->1
		System.out.println(2^3);//1

		//~ -5
		//5 的原码：0000 0000 0000 0101
		//-5的原码：1000 0000 0000 0101
		//-5的反码：1111 1111 1111 1010
		//-5的补码：1111 1111 1111 1011
		//~ -5的补码：0000 0000 0000 0100
		//结果原码：0000 0000 0000 0100->4
		System.out.println(~ -5);
	}
}