-- Review 02:客户端与服务端的本质
-- 场景：面试官问："如果你在公司用的是免费的 DBeaver，而你的同事用的是昂贵的付费版Navicat，
-- 你们俩连着同一个 MySQL 数据库。请问你们写出来的 SQL 代码会有区别吗？"
-- 问题：请用大白话解释图形化工具（GUI）和 MySQL 数据库引I擎之间的真实关系。

-- 完全没有影响！ 哪怕你用公司里最花里胡哨的工具，或者用最古老的黑窗口，
-- 你敲的 SQL 语句（比如 SELECT * FROM user）是100% 一模一样的。

-- 核心原理解析：图形化工具到底是个啥？
-- 不论是 SQLyog、Navicat 还是其他工具，它们在本质上只是一个**“漂亮的外壳（GUI 客户端）”**。
-- 你的 MySQL 8.0 才是真正的“引擎（服务端）”。