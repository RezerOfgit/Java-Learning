-- 去等于表 B 的什么键？（如果你让两张表的主键互相等于，会发生什么灾难？）
-- 多表查询的ON条件必须基于外键-主键的天然关联，主键互等是典型的“逻辑越界”，
-- 会导致数据失真、性能崩溃和业务解释困难。设计查询时，务必先明确表间关系（如1:1、1:N、N:M），
-- 再选择正确的关联字段。


