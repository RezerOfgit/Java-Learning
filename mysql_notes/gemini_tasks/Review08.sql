-- 面试官拷问：“小伙子，既然你昨天说加了索引查询速度能从 4 秒变成 0.003 秒，
-- 那为了让系统彻底起飞，我把表里的每一列都加上索引，这主意贼棒吧？”
-- 
-- 你的任务：请用极其无情的语气反驳他，并用大白话解释，如果真的给每列都加索引，
-- 在用户执行 INSERT（新增数据）或 UPDATE（修改数据）时，数据库底层会经历什么噩梦？

-- 索引不是万金油！盲目全建索引就像给汽车装10个刹车——刹车灵敏了，但油门踩到底也跑不过三轮车！
-- 正确做法是根据查询需求精准建索引（比如WHERE/JOIN常用列），其他列该放生就放生，系统才能真正起飞！
-- INSERT操作变成“索引马拉松”
-- 
-- 原本插入一条数据只需写1次数据页，现在每加一个索引就要多写1次索引页。假设表有10列全建索引，插入1条数据=11次磁盘写入（1数据+10索引）。
-- 数据库底层得疯狂调B+树：每次插入都要调整索引结构，树分裂、节点合并、平衡操作轮番上演，比春运抢票还卡！
-- 
-- UPDATE操作沦为“索引拆迁队”
-- 修改带索引的列？先删旧索引条目，再插新条目，相当于拆了房子再重建。
-- 假设更新姓名列，数据库得先定位姓名索引的叶子节点，删掉旧记录，插入新值，再调整整棵树的平衡。10个索引=10次拆迁+10次重建，比拆迁队还忙！
-- 
-- 存储空间吃成“贪吃蛇”
-- 每个索引都是独立的数据结构，10列索引=10倍存储开销。原本1GB的数据表，加上索引直接飙到10GB，磁盘空间告急，备份恢复慢如蜗牛。