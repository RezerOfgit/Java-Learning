-- 面试官的连环炮 (多表与分组过滤)
-- 
-- 需求 1：请用大白话向面试官解释，在多表查询时，如果不加 WHERE 关联条件，数据库底层会发生什么“大爆炸”？这个现象的专业术语叫什么？
-- 假设你在淘宝后台写代码，商品表有 10 万条数据，用户表有 100 万条数据。如果手滑忘了写 WHERE 条件，
-- 数据库瞬间就会生成 100,000 × 1,000,000 = 1000 亿条无用数据！
-- 这股庞大的数据流会瞬间塞满服务器内存，导致 CPU 飙升到 100%，整个系统当场宕机（俗称“删库跑路”的前奏）。
-- 铁律：写多表查询，绝不能漏掉关联的 WHERE 条件！

-- 需求 2：在 SQL 语句中，如果你想对 GROUP BY 统计出来的结果（比如 COUNT(*) > 1）进行过滤，
-- 你能把这个条件写在 WHERE 后面吗？如果不能，必须用哪个关键字？
-- WHERE：在数据分组（GROUP BY）之前执行。它用于过滤原始的行记录。此时聚合函数（如 COUNT）还没有计算出来，
-- 所以 WHERE 子句中不能使用聚合函数。
-- GROUP BY：将数据分组。
-- HAVING：在数据分组之后执行。它专门用于过滤分组后的结果，因此可以在这里使用聚合函数作为条件。