package com.rezero.dp;

/**
 * @author Re-zero
 * @version 1.0
 * 假设你正在爬楼梯。需要 n 阶你才能到达楼顶。
 * 每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢？
 * 输入: n = 2
 * 输出: 2 (解释: 1. 爬 1阶 + 1阶； 2. 爬 2阶)
 * 输入: n = 3
 * 输出: 3 (解释: 1. 1+1+1； 2. 1+2； 3. 2+1)
 */
public class No70_ClimbingStairs {

    public int climbStairs(int n) {
        // 1. 如果楼梯只有 1 阶或者 2 阶，直接返回对应的数字
        if (n <= 2) {
            return n;
        }

        // 2. 准备一个“小本本”（数组），长度设为 n + 1，
        // 这样我们就可以直接用 dp[n] 来表示第 n 阶的方法数
        int[] dp = new int[n + 1];

        // 3. 记下已知的最基础的答案（初始化）
        dp[1] = 1; // 到第 1 阶有 1 种方法
        dp[2] = 2; // 到第 2 阶有 2 种方法

        // 4. 从第 3 阶开始，一层一层往上推，一直推到第 n 阶
        for (int i = 3; i <= n; i++) {
            // 当前这一阶的方法数，等于前一阶和前两阶的方法数之和
            dp[i] = dp[i - 1] + dp[i - 2];
        }

        // 5. 翻开小本本的最后一页，返回第 n 阶的答案
        return dp[n];
    }
}
